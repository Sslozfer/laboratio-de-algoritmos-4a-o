lugares = ["Italia" , "Turquia" , "Jordania" , "Japon" , "Grecia"]
print (lugares) # normal
print(sorted(lugares)) # sorteado
print (lugares) # normal
print(sorted(lugares , reverse=True)) # sorteado e invertido 
print (lugares) # normal 
lugares.reverse()
print (lugares) # invertido
lugares.reverse()
print (lugares) # normal
lugares.sort()
print (lugares) # sorteado 
lugares.sort(reverse= True)
print (lugares) # sorteado e invertido